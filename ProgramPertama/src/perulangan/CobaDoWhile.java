package perulangan;

public class CobaDoWhile {
    
//    UNTUK MEMBEDAKAN "WHILE" DAN "DO WHILE" ADALAH DENGAN CARA LOGIKA SALAH
    public static void main(String args[])
    {
        int i=1;
        do
        {
            System.out.println(i);
            i++;
        }
        while (i<5);
    }
    
}
